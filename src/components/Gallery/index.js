class Gallery extends Component {
  state = {
    tabId: imagesList[0].id,
    imageUrl: imagesList[0].imageUrl,
    imageAltText: imagesList[0].imageAltText,
  }

  onChangeImg = id => {
    const reqImg = imagesList.find(eachImg => eachImg.id === id)
    this.setState({
      imageUrl: reqImg.imageUrl,
      imageAltText: reqImg.imageAltText,
      tabId: reqImg.id,
    })
  }

  render() {
    const {tabId, imageUrl, imageAltText} = this.state
    return (
      <div className="Photography-cont">
        <div>
          <img src={imageUrl} alt={imageAltText} className="main-image" />
          <div className="desc-cont">
            <h1 className="main-heading">Nature Photography</h1>
            <p className="para">Nature Photography by Rahul</p>
            <ul className="thumnails-cont">
              {imagesList.map(eachImgDetails => (
                <ThunmNail
                  imageDetails={eachImgDetails}
                  key={eachImgDetails.id}
                  onChangeImg={this.onChangeImg}
                  isActive={tabId === eachImgDetails.id}
                />
              ))}
            </ul>
          </div>
        </div>
      </div>
    )
  }
}

export default Gallery
